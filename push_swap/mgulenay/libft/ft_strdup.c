#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*p;
	int		i;

	p = (char *)malloc(sizeof(*s) * (ft_strlen(s) + 1));
	if (p == 0)
	{
		return (NULL);
	}
	i = 0;
	while (s[i] != '\0')
	{
		p[i] = s[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
